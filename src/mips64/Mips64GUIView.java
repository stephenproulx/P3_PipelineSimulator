    private void resetRegs() {
        for (int i = 1; i <= 31; i++) {
            regs[i].setText("0");
        }
    }



    private void activateButtons() {
        RunButton.setEnabled(true);
        SingleStepButton.setEnabled(true);
        Run2BreakButton.setEnabled(true);
        Run2CountButton.setEnabled(true);
    }

    private void deactivateButtons() {
        RunButton.setEnabled(false);
        SingleStepButton.setEnabled(false);
        Run2BreakButton.setEnabled(false);
        Run2CountButton.setEnabled(false);
    }

    private void updateRegs() {
        for (int i = 1; i <= 31; i++) {
            int newValue = simulator.getIdExStage().getIntRegister(i);
            regs[i].setText(Integer.toString(newValue));
        }
    }

    private void updateMemory() {
        Memory4000.setText("");
        Memory5000.setText("");

        for (int i = 0; i < 40; i++) {
            String out = Integer.toString(4000 + 4 * i) + ":  " +
                    Integer.toString(simulator.getMemory().getIntDataAtAddr(4000 + 4 * i)) + "\n";
            Memory4000.append(out);
        }
        Memory4000.setCaretPosition(0);

        for (int i = 0; i < 10; i++) {
            String out = Integer.toString(5000 + 4 * i) + ":  " +
                    Integer.toString(simulator.getMemory().getIntDataAtAddr(5000 + 4 * i)) + "\n";
            Memory5000.append(out);
        }
    }

    private void updateView() {
        updateRegs();
        updateStages();
        updateMemory();

        int newCount = simulator.instExec;
        InstCountField.setText(Integer.toString(newCount));

        if (simulator.memWb.isHalted()){
            deactivateButtons();
        }
    }

