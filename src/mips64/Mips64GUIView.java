        private void resetPipelineView() {
        PC_PC.setText(Integer.toString(simulator.getPCStage().pc));
        // IF/ID
        IFID_PC.setText("");
        IFID_Inst.setText("");

        // ID/EX
        IDEX_PC.setText("");
        IDEX_Inst.setText("");
        IDEX_Reg1Data.setText("");
        IDEX_Reg2Data.setText("");
        IDEX_Immed.setText("");
        IDEX_DoesWB.setSelected(false);



        // EX/MEM
        EXMEM_PC.setText("");
        EXMEM_Inst.setText("");
        EXMEM_ALUData.setText("");
        EXMEM_StoreData.setText("");
        EXMEM_DoesWB.setSelected(false);

        // MEM/WB
        MEMWB_PC.setText("");
        MEMWB_Inst.setText("");
        MEMWB_ALUData.setText("");
        MEMWB_LoadData.setText("");
        MEMWB_DoesWB.setSelected(false);

    }

    private void updateStages() {
        PC_PC.setText(Integer.toString(simulator.pc.pc));

        // IF/ID
        IFID_PC.setText(Integer.toString(simulator.ifId.instPC));
        IFID_Inst.setText(Instruction.getNameFromOpcode(simulator.ifId.opcode));

        // ID/EX
        IDEX_PC.setText(Integer.toString(simulator.idEx.instPC));
        IDEX_Inst.setText(Instruction.getNameFromOpcode(simulator.idEx.opcode));
        IDEX_Reg1Data.setText(Integer.toString(simulator.idEx.regAData));
        IDEX_Reg2Data.setText(Integer.toString(simulator.idEx.regBData));
        IDEX_Immed.setText(Integer.toString(simulator.idEx.immediate));
        if (simulator.idEx.shouldWriteback) {
            IDEX_DoesWB.setSelected(true);
        }
        else {
            IDEX_DoesWB.setSelected(false);
        }


        // EX/MEM
        EXMEM_PC.setText(Integer.toString(simulator.exMem.instPC));
        EXMEM_Inst.setText(Instruction.getNameFromOpcode(simulator.exMem.opcode));
        EXMEM_ALUData.setText(Integer.toString(simulator.exMem.aluIntData));
        EXMEM_StoreData.setText(Integer.toString(simulator.exMem.storeIntData));
        if (simulator.exMem.shouldWriteback) {
            EXMEM_DoesWB.setSelected(true);
        }
        else {
            EXMEM_DoesWB.setSelected(false);
        }

        // MEM/WB
        MEMWB_PC.setText(Integer.toString(simulator.memWb.instPC));
        MEMWB_Inst.setText(Instruction.getNameFromOpcode(simulator.memWb.opcode));
        MEMWB_ALUData.setText(Integer.toString(simulator.memWb.aluIntData));
        MEMWB_LoadData.setText(Integer.toString(simulator.memWb.loadIntData));
        if (simulator.memWb.shouldWriteback) {
            MEMWB_DoesWB.setSelected(true);
        }
        else {
            MEMWB_DoesWB.setSelected(false);
        }
    }
